#include <stdio.h>
#include <stdlib.h>

int main()
{
    //Write a program to display the multiplication table of a given number.
    int n ,i,r;
    printf("enter the number :  ");
    scanf("%d", &n);

    for( i=1 ; i<= 10 ; i++ )
    {
        r==1;
        r=i*n ;

        printf( "%d * %d = %d \n" ,i,n,r );
    }
    return 0;
}
